package com.class02;

public class VariableValues {

	public static void main(String[] args) {
		
		short a=100;
		char b='!';
		double d=8.99;
		boolean c=true;
		
		a=200;
		b='@';
		c=false;
		d=9.99;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		
		

	}

}
